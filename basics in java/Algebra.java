public class Algebra {
    public static void main(String[] args) {
        // Example of a simple algebraic expression: (a + b) * c
        int a = 5;
        int b = 3;
        int c = 2;
       // order of operation [priority] 1. parentheses 2. multiplication and division 3. addition and subtraction
        int result = (a + b) * c; // This will calculate (5 + 3) * 2
        System.out.println("The result of the expression (a + b) * c is: " + result);
    }
}
